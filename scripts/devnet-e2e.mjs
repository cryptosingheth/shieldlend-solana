const NULLIFIER_HASH    = PUBLIC_SIGNALS.slice(17 * 32, 18 * 32); // signal[17]
const SMOKE_ROOT        = PUBLIC_SIGNALS.slice(18 * 32, 19 * 32); // signal[18]
const DENOMINATION_U64  = 100_000_000n; // 0.1 SOL, from signal[0]

// ─── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  const keypairPath = path.join(homedir(), ".config", "solana", "id.json");
  const wallet = Keypair.fromSecretKey(
    Uint8Array.from(JSON.parse(readFileSync(keypairPath, "utf-8")))
  );
  const connection = new Connection("https://api.devnet.solana.com", "confirmed");
  const balance = await connection.getBalance(wallet.publicKey);
  console.log(`Wallet:  ${wallet.publicKey.toBase58()}`);
  console.log(`Balance: ${(balance / 1e9).toFixed(9)} SOL\n`);

  // ── PDAs ──────────────────────────────────────────────────────────────────

  const [poolState]        = pda([Buffer.from("shielded-pool-state")], SHIELDED_POOL);
  const [registryConfig]   = pda([Buffer.from("registry-config")], NULL_REGISTRY);
  const [registryWriter]   = pda([REGISTRY_WRITER_SEED], SHIELDED_POOL);
  const [nullifierPda]     = pda([Buffer.from("nullifier"), NULLIFIER_HASH], NULL_REGISTRY);
  console.log(`PDAs:`);
  console.log(`  pool state:       ${poolState.toBase58()}`);
  console.log(`  registry config:  ${registryConfig.toBase58()}`);
  console.log(`  registry writer:  ${registryWriter.toBase58()}`);
  console.log(`  nullifier PDA:    ${nullifierPda.toBase58()}\n`);

  // ── Step 1: nullifier_registry::initialize ────────────────────────────────

  const registryConfigInfo = await connection.getAccountInfo(registryConfig);
  if (registryConfigInfo) {
    console.log(`SKIP nullifier_registry::initialize (registry-config already exists)`);
  } else {
    // authorized_programs = [shielded_pool, lending_pool]
    // Borsh Vec<Pubkey>: u32 LE length + (32 bytes × n)
    const authorizedPrograms = Buffer.concat([
      Buffer.from([2, 0, 0, 0]), // Vec length = 2
      SHIELDED_POOL.toBytes(),
      LENDING_POOL.toBytes(),
    ]);
    const ix = new TransactionInstruction({
      programId: NULL_REGISTRY,
      keys: [
        { pubkey: wallet.publicKey, isSigner: true,  isWritable: true  },
        { pubkey: registryConfig,   isSigner: false, isWritable: true  },
        { pubkey: SystemProgram.programId, isSigner: false, isWritable: false },
      ],
      data: Buffer.concat([disc("initialize"), authorizedPrograms]),
    });
    await send(connection, wallet, [ComputeBudgetProgram.setComputeUnitLimit({ units: 50_000 }), ix],
      "nullifier_registry::initialize");
  }

  // ── Step 2: shielded_pool::initialize ─────────────────────────────────────

  const poolStateInfo = await connection.getAccountInfo(poolState);
  if (poolStateInfo) {
    console.log(`SKIP shielded_pool::initialize (shielded-pool-state already exists)`);
  } else {
    const ix = new TransactionInstruction({
      programId: SHIELDED_POOL,
      keys: [
        { pubkey: wallet.publicKey, isSigner: true,  isWritable: true  },
        { pubkey: poolState,        isSigner: false, isWritable: true  },
        { pubkey: SystemProgram.programId, isSigner: false, isWritable: false },
      ],
      data: disc("initialize"),
    });
    await send(connection, wallet, [ComputeBudgetProgram.setComputeUnitLimit({ units: 50_000 }), ix],
      "shielded_pool::initialize");
  }

  // ── Step 3: shielded_pool::store_withdraw_proof ───────────────────────────

  const proofNonce = randomBytes(32);
  const [proofDataPda] = pda([PROOF_DATA_SEED, wallet.publicKey.toBytes(), proofNonce], SHIELDED_POOL);

  {
    const ix = new TransactionInstruction({
      programId: SHIELDED_POOL,
      keys: [
        { pubkey: wallet.publicKey, isSigner: true,  isWritable: true  },
        { pubkey: proofDataPda,     isSigner: false, isWritable: true  },
        { pubkey: SystemProgram.programId, isSigner: false, isWritable: false },
      ],
      data: Buffer.concat([disc("store_withdraw_proof"), proofNonce, PROOF_A, PROOF_B, PROOF_C, PUBLIC_SIGNALS]),
    });
    const result = await send(connection, wallet,
      [ComputeBudgetProgram.setComputeUnitLimit({ units: 100_000 }), ix],
      "shielded_pool::store_withdraw_proof");
    if (!result.ok) {
      console.error("\nAbort: proof storage failed, cannot proceed to withdraw attempt.");
      process.exit(1);
    }
  }

  // ── Step 4: shielded_pool::withdraw (expected: UnknownRoot) ───────────────
  // WithdrawArgs Borsh layout:
  //   root[32] | nullifier_hash[32] | denomination_lamports(u64 LE) | stealth_address[32] | relay_nonce(u64 LE) | proof_nonce[32]

  console.log(`\nAttempting withdraw (expected to fail with UnknownRoot — no deposit has been flushed):`);
  {
    const withdrawArgs = Buffer.concat([
      SMOKE_ROOT,                              // root = signal[18]
      NULLIFIER_HASH,                          // nullifier_hash = signal[17]
      u64LE(DENOMINATION_U64),                 // denomination_lamports = 0.1 SOL
      wallet.publicKey.toBytes(),              // stealth_address (placeholder)
      u64LE(0),                                // relay_nonce
      proofNonce,                              // proof_nonce (same as store step)
    ]);
    const ix = new TransactionInstruction({
      programId: SHIELDED_POOL,
      keys: [
        { pubkey: wallet.publicKey, isSigner: true,  isWritable: true  },  // relay
        { pubkey: poolState,        isSigner: false, isWritable: true  },  // state
        { pubkey: nullifierPda,     isSigner: false, isWritable: true  },  // nullifier (CPI target)
        { pubkey: registryConfig,   isSigner: false, isWritable: false },  // registry_config
        { pubkey: registryWriter,   isSigner: false, isWritable: false },  // registry_writer
        { pubkey: NULL_REGISTRY,    isSigner: false, isWritable: false },  // nullifier_registry_program
        { pubkey: SystemProgram.programId, isSigner: false, isWritable: false },
        { pubkey: proofDataPda,     isSigner: false, isWritable: true  },  // proof_data
      ],
      data: Buffer.concat([disc("withdraw"), withdrawArgs]),
    });
    const result = await send(connection, wallet,
      [ComputeBudgetProgram.setComputeUnitLimit({ units: 400_000 }), ix],
      "shielded_pool::withdraw");

    if (result.ok) {
      console.log("\n  NOTE: withdraw succeeded — pool root was already populated.");
    } else {
      const logsStr = result.logs.join(" ");
      if (logsStr.includes("UnknownRoot") || logsStr.includes("0x1777") || logsStr.includes("6007")) {
        console.log("\n  EXPECTED: withdraw failed with UnknownRoot.");
        console.log("  Reason: pool is freshly initialized; no deposit has been flushed to produce");
        console.log("  a Merkle root matching the smoke proof's signal[18] root.");
        console.log("  Account layout, PDA derivation, and proof_data guards all passed before");
        console.log("  the root check. System is working correctly.");
      } else {
        console.log("\n  UNEXPECTED failure. Review logs above.");
      }
    }
  }

  const finalBalance = await connection.getBalance(wallet.publicKey);
  console.log(`\nFinal balance: ${(finalBalance / 1e9).toFixed(9)} SOL`);
}

main().catch((err) => { console.error(err); process.exit(1); });
