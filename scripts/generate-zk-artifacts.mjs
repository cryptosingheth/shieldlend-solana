import {
  copyFileSync,
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  writeFileSync,
} from "node:fs";
import { createHash } from "node:crypto";
import { join } from "node:path";
import { spawnSync } from "node:child_process";
import process from "node:process";

const CIRCUITS = ["withdraw_ring", "collateral_ring", "repay_ring"];
const BUILD_DIR = "build/circuits";
const PUBLIC_DIR = "frontend/public/circuits";
const INCLUDE_DIRS = ["node_modules", "frontend/node_modules"];

function run(cmd, args, options = {}) {
  const result = spawnSync(cmd, args, {
    encoding: "utf8",
    stdio: options.stdio ?? "pipe",
  });

  return {
    status: result.status,
    stdout: result.stdout ?? "",
    stderr: result.stderr ?? "",
    ok: result.status === 0,
  };
}

function requireTool(name, args, acceptsVersionOutput = false) {
  const result = run(name, args);
  const output = `${result.stdout}${result.stderr}`.trim();
  if (!result.ok && !acceptsVersionOutput) {
    throw new Error(`${name} ${args.join(" ")} failed: ${output || "no output"}`);
  }
  if (!result.ok && acceptsVersionOutput && !output.includes(`${name}@`)) {
    throw new Error(`${name} ${args.join(" ")} failed: ${output || "no output"}`);
  }
  console.log(`${name}: ${output.split("\n")[0]}`);
}

function sha256(path) {
  return createHash("sha256").update(readFileSync(path)).digest("hex");
}

function findPtau() {
  const candidates = [".", "circuits", BUILD_DIR]
    .flatMap((dir) => (existsSync(dir) ? readdirSync(dir).map((file) => join(dir, file)) : []))
    .filter((path) => path.endsWith(".ptau"));
  return candidates[0];
}

function compileCircuit(circuit) {
  const source = `circuits/${circuit}.circom`;
  const includeArgs = INCLUDE_DIRS.flatMap((dir) => ["-l", dir]);
  const result = run(
    "circom",
    [source, ...includeArgs, "--r1cs", "--wasm", "--sym", "-o", BUILD_DIR],
    { stdio: "inherit" }
  );
  if (!result.ok) throw new Error(`failed to compile ${source}`);
}

function setupGroth16(circuit, ptauPath) {
  const r1cs = `${BUILD_DIR}/${circuit}.r1cs`;
  const zkey0 = `${BUILD_DIR}/${circuit}_0000.zkey`;
  const zkeyFinal = `${BUILD_DIR}/${circuit}.zkey`;
  const vkey = `${BUILD_DIR}/${circuit}_vkey.json`;

  let result = run("snarkjs", ["groth16", "setup", r1cs, ptauPath, zkey0], {
    stdio: "inherit",
  });
  if (!result.ok) throw new Error(`groth16 setup failed for ${circuit}`);

  result = run(
    "snarkjs",
    [
      "zkey",
      "beacon",
      zkey0,
      zkeyFinal,
      "01020304050607080900112233445566778899aabbccddeeff0011223344556677",
      "10",
    ],
    { stdio: "inherit" }
  );
  if (!result.ok) throw new Error(`zkey beacon failed for ${circuit}`);

  result = run("snarkjs", ["zkey", "export", "verificationkey", zkeyFinal, vkey], {
    stdio: "inherit",
  });
  if (!result.ok) throw new Error(`verification key export failed for ${circuit}`);
}

function copyBrowserArtifacts(hasZkeys) {
  mkdirSync(PUBLIC_DIR, { recursive: true });

  const manifest = {};
  for (const circuit of CIRCUITS) {
    const name = circuit.replace("_ring", "");
    const wasmSource = `${BUILD_DIR}/${circuit}_js/${circuit}.wasm`;
    const wasmDest = `${PUBLIC_DIR}/${circuit}.wasm`;
    copyFileSync(wasmSource, wasmDest);

    manifest[name] = {
      wasm: {
        path: `/circuits/${circuit}.wasm`,
        sha256: sha256(wasmDest),
      },
      zkey: {
        path: `/circuits/${circuit}.zkey`,
        sha256: null,
      },
      vkey: {
        path: `/circuits/${circuit}_vkey.json`,
        sha256: null,
      },
    };

    if (hasZkeys) {
      const zkeyDest = `${PUBLIC_DIR}/${circuit}.zkey`;
      const vkeyDest = `${PUBLIC_DIR}/${circuit}_vkey.json`;
      copyFileSync(`${BUILD_DIR}/${circuit}.zkey`, zkeyDest);
      copyFileSync(`${BUILD_DIR}/${circuit}_vkey.json`, vkeyDest);
      manifest[name].zkey.sha256 = sha256(zkeyDest);
      manifest[name].vkey.sha256 = sha256(vkeyDest);
    }
  }

  writeFileSync("circuits/artifact_manifest.json", `${JSON.stringify(manifest, null, 2)}\n`);
}

function writeCeremony({ ptauPath, hasZkeys }) {
  const body = `# ZK Ceremony Notes

Generated by \`scripts/generate-zk-artifacts.mjs\`.

- Circuits: ${CIRCUITS.join(", ")}
- Proving system: Groth16 over BN254
- Powers of Tau input: ${ptauPath ?? "not found"}
- ZKey generation: ${hasZkeys ? "completed" : "blocked"}

If a production or shared ceremony is used later, replace this note with the
ceremony provenance, participant transcript, verification commands, and hashes.
Do not claim production privacy from locally generated or unverified setup
artifacts.
`;

  writeFileSync("circuits/CEREMONY.md", body);
}

function main() {
  mkdirSync(BUILD_DIR, { recursive: true });

  const constants = run("node", ["scripts/derive-program-id-field.mjs", "--write"], {
    stdio: "inherit",
  });
  if (!constants.ok) throw new Error("failed to derive circuit constants");

  requireTool("circom", ["--version"]);
  requireTool("snarkjs", ["--version"], true);

  for (const circuit of CIRCUITS) compileCircuit(circuit);

  const ptauPath = findPtau();
  const hasZkeys = Boolean(ptauPath);

  if (ptauPath) {
    for (const circuit of CIRCUITS) setupGroth16(circuit, ptauPath);
  } else {
    console.warn("No .ptau file found; skipping Groth16 zkey and verification-key generation.");
  }

  copyBrowserArtifacts(hasZkeys);
  writeCeremony({ ptauPath, hasZkeys });
}

main();
