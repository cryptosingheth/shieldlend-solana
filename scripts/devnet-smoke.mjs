// ─── Main ─────────────────────────────────────────────────────────────────────

async function main() {
  const keypairPath = path.join(homedir(), ".config", "solana", "id.json");
  const wallet = Keypair.fromSecretKey(
    Uint8Array.from(JSON.parse(readFileSync(keypairPath, "utf-8")))
  );
  console.log(`Wallet:  ${wallet.publicKey.toBase58()}`);

  const connection = new Connection("https://api.devnet.solana.com", "confirmed");
  const balance = await connection.getBalance(wallet.publicKey);
  console.log(`Balance: ${(balance / 1e9).toFixed(9)} SOL`);

  // Fresh nonce per run so the PDA is unique (init would fail on re-use)
  const proofNonce = randomBytes(32);

  const [proofDataPda] = PublicKey.findProgramAddressSync(
    [PROOF_DATA_SEED, wallet.publicKey.toBytes(), proofNonce],
    SHIELDED_POOL_PROGRAM_ID
  );
  console.log(`proof_data PDA: ${proofDataPda.toBase58()}`);

  // Borsh-encoded instruction data (fixed arrays = raw bytes, no length prefix)
  // Layout: discriminator(8) | proof_nonce(32) | proof_a(64) | proof_b(128) | proof_c(64) | public_inputs(608)
  const ix_data = Buffer.concat([
    discriminator("store_withdraw_proof"), // 8
    proofNonce,                            // 32
    PROOF_A,                               // 64
    PROOF_B,                               // 128
    PROOF_C,                               // 64
    PUBLIC_SIGNALS,                        // 608
  ]);
  console.log(`Instruction data: ${ix_data.length} bytes (expected 904)`);

  const ix = new TransactionInstruction({
    programId: SHIELDED_POOL_PROGRAM_ID,
    keys: [
      { pubkey: wallet.publicKey, isSigner: true,  isWritable: true  }, // authority
      { pubkey: proofDataPda,     isSigner: false, isWritable: true  }, // proof_data (init)
      { pubkey: SystemProgram.programId, isSigner: false, isWritable: false }, // system_program
    ],
    data: ix_data,
  });

  const tx = new Transaction().add(
    ComputeBudgetProgram.setComputeUnitLimit({ units: 100_000 }),
    ix
  );
  tx.feePayer = wallet.publicKey;
  tx.recentBlockhash = (await connection.getLatestBlockhash()).blockhash;

  console.log("\nSending store_withdraw_proof to devnet...");
  try {
    const sig = await sendAndConfirmTransaction(connection, tx, [wallet], {
      commitment: "confirmed",
    });
    console.log(`\nSUCCESS`);
    console.log(`Signature: ${sig}`);
    console.log(`Explorer:  https://explorer.solana.com/tx/${sig}?cluster=devnet`);
  } catch (err) {
    console.error(`\nFAILED: ${err.message}`);
    if (err.logs && err.logs.length > 0) {
      console.error("Program logs:");
      err.logs.forEach((l) => console.error(`  ${l}`));
    }
    process.exit(1);
  }
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
